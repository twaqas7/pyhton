# Description: For loop to iterate over list, tuple, string

# for loop with list   # list is iterable

l = [1, 25, 35, 45, 55, 65, 75, 85, 95, 105]

for i in l:
    print(i)


# for loop with tuple  # tuple is also iterable

t = (1, 25, 35, 45, 55, 65, 75, 85, 95, 105)

for i in t:
    print(i)

# for loop with string  # string is also iterable

name = "Tahir"

for i in name:
    print(i)  # Tahir
